   /**
    * This function return the zip code of an theater
    *
    * @param [type] $price
    * @return array
    */
    public function findCodeBy($id): array
{
    $conn = $this->getEntityManager()->getConnection();

    $sql = '
        SELECT * FROM user AS us
        JOIN theater As th
        ON th.user_id=us.id
        WHERE th.zip_code :id        
        ';
    $stmt = $conn->prepare($sql);
    $stmt->execute(['zip_code' => $id]);

    // returns an array of arrays (i.e. a raw data set)
    return $stmt->fetchAll();
}



